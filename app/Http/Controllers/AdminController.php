     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $admin = new Admin;
        $admin->kategori = $request->kategori;
        $admin->nama = $request->nama;
        $admin->harga = $request->harga;
        $admin->deskripsi = $request->deskripsi;
        
        $file = $request->file('file');
        $namafile = $request->exampleFormControlFile1;
        //$tujuan = 'public/images/menu';
        $file->move(public_path('images/menu'),$namafile);

        $admin->gambar = $namafile;

        $admin->save();
        return redirect('/admin');
    }

